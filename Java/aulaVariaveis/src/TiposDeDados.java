public class TiposDeDados {

    public static void main(String[] args) {

        /*

        byte;
        short;
        int;
        long;
        float; sempre usar 3.14(f)
        double;

        char; usar 'aspas simples'
        String; usar "aspas duplas"

        boolean;

         */

        byte b1 = 10;
        System.out.println(b1);

        byte b2 = 15;
        System.out.println(b2);

        short s1 = 20000;
        System.out.println(s1);

        //short s2 = 40000;

        //int i1 = -10000000000;
        int i2 = 28500;
        System.out.println(i2);

        long l1 = 100000000000000000L;
        System.out.println(l1);

        //long l2 = 24501600000000000000;

        float f1 = 4.5f;
        System.out.println(f1);

        //float f2 = 4.56;

        double d1 = 85.69;
        System.out.println(d1);

        double d2 = 89.65d;
        System.out.println(d1);

        char c1 = 'W';
        System.out.println(c1);

        //char c2 = "W";

        //char c3 = 'ch';

        char c4 = '\u0057'; // codigo unicode da letra W
        System.out.println(c4);

        String st1 = "Dani";
        System.out.println(st1);

        //String st2 = 'Dani';

        String st3 = "ffdsgdfg fdfgdfgdf fgdfgdfg ";
        System.out.println(st3);

        //String date = "09/12/2005";

        boolean bo1 = true;
        System.out.println(bo1);

        boolean bo2 = false;
        System.out.println(bo2);

    }
}
